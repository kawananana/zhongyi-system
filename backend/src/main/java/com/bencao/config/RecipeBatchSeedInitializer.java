    private void upsertOne(String recipeName,
                           String category,
                           String coverImage,
                           String summary,
                           String efficacy,
                           String cookingSteps,
                           String cookingTime,
                           String difficulty,
                           String tags,
                           String constitutionTags,
                           Integer isFeatured,
                           Integer viewCount,
                           Integer status) {
        Recipe recipe = recipeMapper.selectOne(new LambdaQueryWrapper<Recipe>()
                .eq(Recipe::getRecipeName, recipeName)
                .last("LIMIT 1"));
        if (recipe == null) {
            recipe = new Recipe();
        }
        recipe.setRecipeName(recipeName);
        recipe.setCategory(category);
        recipe.setCoverImage(StringUtils.hasText(coverImage) ? coverImage : null);
        recipe.setSummary(summary);
        recipe.setEfficacy(efficacy);
        recipe.setCookingSteps(cookingSteps);
        recipe.setCookingTime(cookingTime);
        recipe.setDifficulty(difficulty);
        recipe.setTags(tags);
        recipe.setConstitutionTags(constitutionTags);
        recipe.setIsFeatured(isFeatured);
        if (recipe.getId() == null) {
            recipe.setViewCount(0);
        }
        recipe.setStatus(status);

        if (recipe.getId() == null) {
            recipeMapper.insert(recipe);
        } else {
            recipeMapper.updateById(recipe);
        }
        log.info("已同步食谱: {}", recipeName);
    }
